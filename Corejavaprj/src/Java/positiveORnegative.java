package Java;

import java.util.Scanner;

public class positiveORnegative {

	public static void main(String[] args) {
    Scanner ab=new Scanner(System.in);
    int a=ab.nextInt();
    if(a>0)
    {
    	System.out.println("Num is positive");
    }
    else
    {
    	System.out.println("Num is negative");
    }
	}

}
